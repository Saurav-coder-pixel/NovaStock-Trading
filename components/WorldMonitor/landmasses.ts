// Convert lat/lon to SVG x,y for 2D map
export function toSVG(lat: number, lon: number, W = 1000, H = 500): [number, number] {
  return [
    Math.round((lon + 180) / 360 * W),
    Math.round((90 - lat) / 180 * H),
  ];
}

// Build SVG path string from landmass coords
export function landmassSVGPath(coords: [number, number][], W = 1000, H = 500): string {
  return coords.map(([lat, lon], i) => {
    const [x, y] = toSVG(lat, lon, W, H);
    return `${i === 0 ? 'M' : 'L'}${x},${y}`;
  }).join(' ') + ' Z';
}
